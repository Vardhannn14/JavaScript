// Task: Write a program to check if a number is between 10 and 20 using logical operators.
// ubric: 2 points for correct condition, 3 points for output.

let num = "20";
number = num;

function inrange(number) {
    
    if (number >= 10 && number <= 20) {
        return "The number is between 10 and 20.";
    } else {
        return "The number is not between 10 and 20.";
    }
}

console.log(inrange(number)); 

