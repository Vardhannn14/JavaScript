let day = "Sunday";

switch (day) {
    case "Monday":
        console.log("સોમવાર");
        break;
    case "Tuesday":
        console.log("મંગળવાર");
        break;
    case "Wednesday":
        console.log("બુધવાર")
        break;
    case "Thursday":
        console.log("ગુરુવાર");
        break;
    case "Friday":
        console.log("શુક્રવાર");
        break;
    case "Saturday":
        console.log("શનિવાર");
        break;
    case "Sunday":
        console.log("રવિવાર");
        break;
    default:
        console.log("Invalid");    
}